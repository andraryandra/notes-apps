/** Struktur sama dengan `id`, nilai leaf boleh string apa pun (untuk locale lain). */
export type DeepStringMap<T> = T extends string
  ? string
  : T extends Record<string, unknown>
    ? { [K in keyof T]: DeepStringMap<T[K]> }
    : never;

export type TranslationTree = DeepStringMap<typeof id>;
